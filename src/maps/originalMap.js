// ============================================================
// PANZADROME - ORIGINAL MAP DATA
// Transcribed from the 1985 ZX Spectrum version
// Based on the Speccy Screenshot Maps composite by MLT (2005)
// ============================================================
//
// Each screen is a 20×14 grid (COLS × ROWS)
// Tile types: 0=Empty, 1=Wall, 2=Vent, 3=Factory:Mortar, 4=Factory:Mine,
//   5=Factory:Polycrete, 6=Factory:Shield, 7=Factory:Speed, 8=Factory:Ricochet, 9=GunEmplacement
//
// The map is 8×8 screens. Coordinates are [row][col] where
// row 0 = top of island, col 0 = left of island
//
// Exit gaps: Top/Bottom edges at columns 7-12, Left/Right edges at rows 5-8
// Border walls are added dynamically by the engine based on adjacency

// Helper: parse a compact string representation into a 2D array
// '.' = empty, '#' = wall, 'V' = vent, 'G' = gun emplacement
// 'M' = factory:mortar, 'N' = factory:mine, 'P' = factory:polycrete
// 'S' = factory:shield, 'D' = factory:speed, 'R' = factory:ricochet
const CHAR_MAP = {
  '.': 0, '#': 1, 'V': 2, 'M': 3, 'N': 4, 'P': 5, 'S': 6, 'D': 7, 'R': 8, 'G': 9,
};

function parseScreen(rows) {
  return rows.map(row => 
    row.split('').map(ch => CHAR_MAP[ch] ?? 0)
  );
}

