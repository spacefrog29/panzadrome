// Enemy counts per screen (base count, guards are placed at vent screens)
// Difficulty scales with distance from start [0,0]
export function getEnemyCount(row, col) {
  const dist = row + col;
  const isVentScreen = ventScreens.some(([r, c]) => r === row && c === col);
  const base = 1 + Math.floor(dist / 2);
  const guards = isVentScreen ? 4 : 0;
  return { regular: Math.min(base, 4), guards };
}

